                <div class="col-xs-12 col-sm-8 col-md-6 col-lg-4"><h4 style="font-style:italic; font-weight: bold">Histoire du Wado Ryu</h4>
                <p>C’est en 1934 que le style de karaté Wado Ryu est né. Il a été fondé par Hironori OTSUKA, pratiquant de Jujitsu et de Karaté. C’est en s’inspirant principalement de ces 2 arts martiaux que OTSUKA SENSEI repense le karaté d’Okinawa pour le rendre plus fluide et plus arrondi. Ainsi, le Karaté Wado Ryu est régi par 3 grands principes : Noru, Nagasu et Inasu.</p>
                <p> <p style="font-weight: bold; display:inline-block; margin-bottom: auto;">Noru</p> signifie rentrer, contrer, pénétrer. Il fait référence au concept de sen no sen : l'attaque dans l'attaque. Il s'agit de pénétrer dans l'attaque de l'adversaire pour le contrer.</p>
                <p> <p style="font-weight: bold; display:inline-block; margin-bottom: auto;">Nagasu</p> se traduit par aspirer, accompagner, laisser passer et enrouler. Il s'agit d'effectuer une esquive et/ou une déviation de l'attaque pour se retrouver dans un placement avantageux pour répliquer. En effet, l'une des spécificités du karaté Wado Ryu est de ne pas faire appel aux blocages "en force" mais de privilégier le déplacement et les absorptions.</p>
                <p><p style="font-weight: bold; display:inline-block; margin-bottom: auto;">Inasu</p> est la combinaison de Noru et de nagasu que l'on peut résumer par "l'action dans l'esquive". Il s'agit donc de dévier la trajectoire de l'attaque par une esquive accompagnée d'une parade (nagasu) en contrant simultanément (noru). En cela, la maîtrise du principe de Inasu est l'aboutissement du karaté Wado Ryu.</p></div>
