        <section>
            <h4 class="center-moral"><strong>LA FIDELITE : CHUJITSU</strong></h4>
            <center><img class="moral-img" src="../asset/img/Chujitsu.jpg" alt="code moral karaté Chujitsu"></center>
            
            <p class="moral-text">Il n'y a pas d'honneur sans fidélité et loyauté à l'égard de certains idéaux et de ceux qui les partagent. La fidélité symbolise la nécessité incontournable de tenir ses promesses et remplir ses engagements. La fidélité nécessite la sincérité dans les paroles et dans les actes.</p>
        </section>
        <br>
        <section>
            <h4 class="center-moral"><strong>LA SINCERITE : MAKOTO</strong></h4>
            <center><img class="moral-img" src="../asset/img/Makoto.jpg" alt="code moral karaté Makoto"></center>
            
            <p class="moral-text">Lors du salut du karatéka,, vous exprimez cette sincérité. Le mensonge et l'équivoque engendrent la suspicion qui est la source de toutes les désunions. Dans les arts martiaux, le salut est l'expression de cette sincérité, c'est le signe de celui qui ne déguise ni ses sentiments, ni ses pensées de celui qui sait être authentique.</p>
        </section>
        <br>
        <section>
            <h4 class="center-moral"><strong>LE COURAGE : YÜKI</strong></h4>
            <center><img class="moral-img" src="../asset/img/Yuki.jpg" alt="code moral karaté Yuki"></center>
            
            <p class="moral-text">La force d'âme qui fait braver le danger et la souffrance s'appelle le courage. Ce courage qui nous pousse à faire respecter ce qui nous paraît juste et qui nous permet malgré nos peurs et nos craintes d'affronter toutes les épreuves. La bravoure, l'ardeur et surtout la volonté sont les supports du courage.</p>
        </section>
        <br>
        <section>
            <h4 class="center-moral"><strong>LA BONTE ET LA BIENVEILLANCE : SHINSETSU</strong></h4>
            <center><img class="moral-img" src="../asset/img/Shinsetsu.jpg" alt="code moral karaté Shinsetsu"></center>
            
            <p class="moral-text">La bonté est une marque de courage qui dénote une haute humanité. Elle nous pousse à l'entraide, à être attentif à notre prochain et à notre environnement  à être respectueux de la vie.</p>
        </section>
        <br>
        <section>
            <h4 class="center-moral"><strong>LA MODESTIE ET L'HUMILITE : KYOKEN</strong></h4>
            <center><img class="moral-img" src="../asset/img/Kyoken.jpg" alt="code moral karaté Kyoken"></center>
            
            <p class="moral-text">Si le budoka devient l'ambassadeur du  code moral, il se doit de rester humble et ne pas flatter son égo. La bonté et la bienveillance ne peuvent s'exprimer sincèrement sans modération dans l'appréciation de soi-même. Savoir être humble, exempt d'orgueil et de vanité est le seul garant de la modestie.</p>
        </section>
        <br>
        <section>
            <h4 class="center-moral"><strong>LA DROITURE : TADASHI</strong></h4>
            <center><img class="moral-img" src="../asset/img/Tadashi.jpg" alt="code moral karaté Tadashi"></center>
            
            <p class="moral-text">C'est suivre la ligne du devoir sans jamais s'en écarter. Loyauté, honneteté et sincérité sont les piliers de cette droiture. Elles nous permettent de prendre sans aucune faiblesse une décision juste et raisonnable. La droiture engendre le respect à l'égard  des autres et de la part des autres. La politesse est l'expression de ce respect dû à autrui.</p>
        </section>
