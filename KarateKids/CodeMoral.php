            <center><img class="moral-img" src="../asset/img/Sonchoo.jpg" alt="code moral karaté Sonchoo"></center>
            
            <p class="moral-text">La droiture engendre le respect à l'égard des autres et de la part des autres. Quelques que soient ses qualiés, ses faiblesses ou sa position sociale savoir traiter les personnes et les choses avec déférence et respecter le sacré est le premier devoir d'un Budoka car cela permet d'éviter de nombreuses querelles et conflits.</p>
        </section>
