              Les écoles Shito-Ryu et Shotokan possèdent deux variantes de Bassaï, la grande (Daï) et la petite (Sho). Une seule forme est enseignée en Wado-Ryu, elle correspond à la version « Daï ». Ce Kata est très certainement d’origine chinoise, mais le nom de son créateur est inconnu. Bassaï a été transmis par les écoles d’Okinawa, en particulier Shuri-Te et Tomari-Te. La version « Sho » serait plus récente, vraisemblablement crée par maître Anko Itosu. La traduction la plus répandue de ce Kata est « traverser la forteresse ». Le début du Kata est l’ouverture d’une brèche dans la défense ennemie ; les mouvements suivants, puissants et dynamiques, traduisent la stratégie de vaincre en territoire ennemi.</p>
