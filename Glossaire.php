      <section>
          <h4>Daichi (Position)</h4>
          <ul>
              <li><strong>Heisohu datchi : </strong>debout les 2 pieds l'un contre l'autre</li>
              <li><strong>Musubi datchi : </strong>debout talons joints et les pointes de pieds écartées</li>
              <li><strong>Heiko datchi : </strong>debout pieds écartés et parallèles</li>
              <li><strong>Hachiji datchi : </strong>debout pointes de pieds vers l'extérieur et écartées de la largeur des hanches</li>
              <li><strong>Uchi hachiji datchi : </strong>debout pointes de pieds vers l'intérieur et écartées de la largeur des hanches</li>
              <li><strong>Reinoji datchi : </strong>debout en formant un L</li>
              <li><strong>Teiji datchi : </strong>debout en formant un T</li>
              <li><strong>Junzuki no ashi : </strong>jambe avant fléchie avec 75% du poids du corps et jambe arrière tendue</li>
              <li><strong>Moto datchi : </strong>petit Junzuki no ashi</li>
              <li><strong>Mahamni no nekoashi datchi : </strong>jambe arrière fléchie et pied vers l'extérieur et jambe avant légèrement fléchie avec talon décollé, les 2 talons sur la même ligne</li>
              <li><strong>Hamni no nekoashi datchi : </strong>idem que dessus sauf que les 2 pieds sont à 90°</li>
              <li><strong>Naifantchi datchi : </strong>jambes fléchies les 2 pieds sur la même ligne et parallèles (position du cavalier)</li>
              <li><strong>Shiko datchi : </strong>idem que dessus sauf que les 2 pieds sont vers l'extérieur</li>
              <li><strong>Nekoashi datchi : </strong>Nivejambe arrière fléchie et jambe avant légèrement fléchie avec talon décollé (position du chat)</li>
              <li><strong>Shomen no nekoashi datchi : </strong>idem que dessus sauf que la jambe avant est un peu plus loin</li>
              <li><strong>Seishan datchi :</strong>orteils du pied arrière sur la même ligne du talon avant et pointe du pied avant vers l'intérieur (position du sablier)</li>
              <li><strong>Tate seishan </strong>(petit) : idem que dessus sauf que la jambe avant est un peu plus loin de 20 cm environ</li>
              <li><strong>Tate seishan </strong>(grand) : jambe avant fléchie les 2 pieds sur la même ligne et parallèles</li>
              <li><strong>Fudo datchi : </strong>position de combat</li>
              <li><strong>Tsuru ashi datchi : </strong>debout sur une jambe</li>
          </ul>
